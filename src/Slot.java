public class Slot{
	private Integer slotNumber;
	private String registrationNumberPlate;
	private Integer driverAge;
	public Slot(Integer n){
		this.slotNumber=n;
	}
	
	public Integer getSlotNumber() {
		return slotNumber;
	}
	public void setSlotNumber(Integer slotNumber) {
		this.slotNumber = slotNumber;
	}
	public String getRegistrationNumberPlate() {
		return registrationNumberPlate;
	}
	public void setRegistrationNumberPlate(String registrationNumberPlate) {
		this.registrationNumberPlate = registrationNumberPlate;
	}
	public Integer getDriverAge() {
		return driverAge;
	}
	public void setDriverAge(Integer driverAge) {
		this.driverAge = driverAge;
	}
	
	

}
